    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        if(jTNomes.getSelectedRow() != -1){ // se for diferente de -1, foi selecionado alguma linha na lista
            
            Curso_alunoDAO cadao = new Curso_alunoDAO();
           
            int matricula = (int)jTNomes.getValueAt(jTNomes.getSelectedRow(), 0);
            Curso_aluno ca = cadao.findId(matricula);            
            
            cadao.deleteAluno(ca);
                    
            readMatriculaAluno();
         } else {
            JOptionPane.showMessageDialog(null, "Selecione uma matrícula para excluir");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

