    public void readMatriculaAluno(){
        DefaultTableModel dmtMatricula = (DefaultTableModel) jTNomes.getModel();
        dmtMatricula.setNumRows(0); // Seta o número de linhas como 0 para não cadastrar na lista duas vezes os mesmos dados
        Curso_alunoDAO cadao = new Curso_alunoDAO();
        
        for(Curso_aluno ca: cadao.findAll()){ // Percorremos o objeto setando a lista findAll no Aluno 
            
            dmtMatricula.addRow(new Object[]{ //Passamos os valores vindo do banco de dados nesse vetor
                ca.getCodigo(),
                ca.getAluno(),
                ca.getCurso()
            });
        }
    }

